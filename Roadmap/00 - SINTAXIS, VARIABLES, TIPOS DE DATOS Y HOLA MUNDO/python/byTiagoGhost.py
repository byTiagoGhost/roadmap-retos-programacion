# Variables en Python
x = 5 # Esto representa una variable de tipo entero 
print(x)
y = "Santiago" # Esto representa una variable de tipo cadena de texto
print(y)
z = 3.5 # Esto representa una variable de tipo flotante
print(z)
w = True # Esto representa una variable de tipo booleano
print(w)
x, y, z = "Santiago", "Tiago", "Ghost" # Esto representa varias variables de tipo cadena de texto en una sola linea de Codigo
print(x)
print(y)
print(z)

# Y tambien puedo dejarlas ya especificadas o mas bien dicho ya declaradas
a = str(3) # Esto representa una variable de tipo cadena de texto en numero
print(a)
a = str("Tiago") # Y esto con texto representa una variable de tipo cadena de texto
print(a)
b = int(3) # Esto representa una variable de tipo entero
print(b)
c = float(3) # Esto representa una variable de tipo flotante
print(c)

